#this class contains function to calculate values A and B

class Calculator:
        
    #function to sum
    def sum(a,b):
        return a+b

    #function to subtract
    def subtract(a,b):
        return a-b

    #function to multiply
    def multiply(a,b):
        return a*b

    #function to divide
    def divide(a,b):
        return a/b

    #function to calculate power
    def power(a,b):
        return a**b
